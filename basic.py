# basic_script.py
def add_numbers(a, b):
    """Function to add two numbers."""
    return a + b

def subtract_numbers(a, b):
    """Function to subtract two numbers."""
    return a - b

if __name__ == "__main__":
    print("Addition of 5 and 3:", add_numbers(5, 3))
    print("Subtraction of 5 and 3:", subtract_numbers(5, 3))